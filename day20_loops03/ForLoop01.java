package day20_loops03;

public class ForLoop01 {
    public static void main(String[]args) {
    	for(int i= 1; i <=5; i++) {
    		System.out.print(i);
    		System.out.println("Happy Birthday!");
    	}
    	
    	for(int j =5; j<=100; j+=50) {
    		System.out.println("Congratulations! -" + j);
    	}
    }
    
}
