package day7_conditionals;

public class Chars {
public static void main(String[]args) {
	
	char letter = 'a';
	letter ++;
	
	System.out.println(letter);
	
	int ascii = letter;
	
	System.out.println(ascii);
	
	char myChar= 'D';
	System.out.println(myChar);
	
	--myChar;
	System.out.println(myChar);
	
	char symbol= ')';
	symbol ++;
	System.out.println(symbol);

	char ch = 10100;
	System.out.println(ch);
}
}
